# Проверка, что все символы строки являются буквами
data = 'hello'
print(data.isalpha())


# Проверка, что все символы строки являются цифрами
data = '373274242427427'
print(data.isdigit())

# Проверка, что все символы строки являются символами или буквами
data = 'hello'
print(data.isalnum())


# Проверка, что все символы строки являются пробелами
data = 'hello'
print(data.isspace())

# Проверка регистра строки
data= 'Hello'
print(data.islower())
print(data.isupper())


